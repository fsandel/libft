int	ft_atoi(const char *str)
{
	int	i;
	int	out;
	int	vz;

	i = 0;
	out = 0;
	vz = 1;
	while (str[i] > 0 && str[i] <= ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			vz *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		out += str[i] - '0';
		if (str[i + 1] >= '0' && str[i + 1] <= '9')
			out *= 10;
		i++;
	}
	return (out * vz);
}
